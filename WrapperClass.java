import java.util.*;

// Wrapper class is used to wrape(Convert) primitive datatype into objet(java.lang)
public class WrapperClass {
    public static void main(String[] args) {
        int x=25;
        Integer y=new Integer(x);
        System.out.println("value of primitive type x="+x);
        System.out.println("value of object y="+y);
    }
    
}
